package com.abbot.schimneylife.pojo.media;

public class Media {

	private Integer id;
	private String type;
	private String media_id;
	private Integer created_at;
	private String image_url;
	private String author;
	private String content;
	private String image_media_id;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getMedia_id() {
		return media_id;
	}
	public void setMedia_id(String media_id) {
		this.media_id = media_id;
	}
	public Integer getCreated_at() {
		return created_at;
	}
	public void setCreated_at(Integer created_at) {
		this.created_at = created_at;
	}
	public String getImage_url() {
		return image_url;
	}
	public void setImage_url(String image_url) {
		this.image_url = image_url;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getImage_media_id() {
		return image_media_id;
	}
	public void setImage_media_id(String image_media_id) {
		this.image_media_id = image_media_id;
	}
	
}
