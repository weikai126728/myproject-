package com.abbot.schimneylife.pojo.weixin;

public class AllianceCommission {

	private Integer id;
	private Integer percent;
	private String product_id;
	private Integer pri_status;
	private Integer business_type_id;
	private String typeName;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getPercent() {
		return percent;
	}
	public void setPercent(Integer percent) {
		this.percent = percent;
	}
	public String getProduct_id() {
		return product_id;
	}
	public void setProduct_id(String product_id) {
		this.product_id = product_id;
	}
	
	public Integer getPri_status() {
		return pri_status;
	}
	public void setPri_status(Integer pri_status) {
		this.pri_status = pri_status;
	}
	public Integer getBusiness_type_id() {
		return business_type_id;
	}
	public void setBusiness_type_id(Integer business_type_id) {
		this.business_type_id = business_type_id;
	}
	public String getTypeName() {
		return typeName;
	}
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}
	
}
