package com.abbot.schimneylife.pojo.shopping;

public class CoalitionComm {

	private int id;
	private Integer total;
	private Integer championsComm;
	private Integer agentComm;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Integer getTotal() {
		return total;
	}
	public void setTotal(Integer total) {
		this.total = total;
	}
	public Integer getChampionsComm() {
		return championsComm;
	}
	public void setChampionsComm(Integer championsComm) {
		this.championsComm = championsComm;
	}
	public Integer getAgentComm() {
		return agentComm;
	}
	public void setAgentComm(Integer agentComm) {
		this.agentComm = agentComm;
	}
	
}
