package com.abbot.schimneylife.pojo.shopping;

public class SuperWeiXinOrder {

	private String id;
	private String superInfoId;
	private Double money;
	private String nickName;
	private String createTime;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSuperInfoId() {
		return superInfoId;
	}
	public void setSuperInfoId(String superInfoId) {
		this.superInfoId = superInfoId;
	}
	public Double getMoney() {
		return money;
	}
	public void setMoney(Double money) {
		this.money = money;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	
}
